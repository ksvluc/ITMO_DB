select distinct "Н_ЛЮДИ"."ИМЯ", "Н_ВЕДОМОСТИ"."ЧЛВК_ИД", "Н_СЕССИЯ"."ЧЛВК_ИД"
from "Н_ЛЮДИ"
         inner join "Н_ВЕДОМОСТИ" on "Н_ЛЮДИ"."ИД" = "Н_ВЕДОМОСТИ"."ЧЛВК_ИД"
         inner join "Н_СЕССИЯ" on "Н_ВЕДОМОСТИ"."СЭС_ИД" = "Н_СЕССИЯ"."СЭС_ИД"
where "Н_ЛЮДИ"."ОТЧЕСТВО" = 'Александрович' and "Н_ВЕДОМОСТИ"."ЧЛВК_ИД" < 153285